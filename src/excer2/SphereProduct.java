package excer2;

public class SphereProduct {

    private int r;
    private int h;

    public SphereProduct(int r, int h) {
        this.r = r;
        this.h = h;
    }
    public int getR() {
        return r;
    }
    public void setR(int r) {
        this.r = r;
    }
    public int getH() {
        return h;
    }
    public void setH(int h) {
        this.h = h;
    }
}
